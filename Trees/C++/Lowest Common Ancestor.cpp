/*Algorithm:
We will traverse left and right subtree from root, if any one the root is found then we will return that root.
Lowest ancestor will be that node from which we will get n1 and n2 from its right and left subtree.

*/

#include <bits/stdc++.h>
using namespace std;

// Tree Node
struct Node
{
    int data;
    Node* left;
    Node* right;
};


class Solution
{
    public:
    //Function to return the lowest common ancestor in a Binary Tree.
    Node* lca(Node* root ,int n1 ,int n2 )
    {
       if(!root) //base condition
        return NULL;
        
        if(root->data==n1 || root->data==n2) //if node is found
            return root;
            
        Node *l=lca(root->left,n1,n2 ); //traverse left subtree
        Node *r=lca(root->right,n1,n2 ); //traverse right subtree
         
         if(l!=NULL && r!=NULL) //lowest ancestor found
            return root;
            
        if(l!=NULL)
            return l;
        else
            return r;
    }
};

